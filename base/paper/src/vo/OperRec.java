//操纵资源
package vo;

import java.sql.Timestamp;

public class OperRec {
	private int id;
	private String TableName;
	private int DoType;
	private boolean IDType;
	private String old_id;
	private String new_id;
	private Timestamp ApplyDate;
	private Timestamp VerifyDate;// 核定时间
	private int flg;
	private int applyer;
	private String cause;// 原因

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getTableName() {
		return TableName;
	}

	public void setTableName(String tableName) {
		TableName = tableName;
	}

	public int getDoType() {
		return DoType;
	}

	public void setDoType(int doType) {
		DoType = doType;
	}

	public boolean isIDType() {
		return IDType;
	}

	public void setIDType(boolean iDType) {
		IDType = iDType;
	}

	public String getOld_id() {
		return old_id;
	}

	public void setOld_id(String old_id) {
		this.old_id = old_id;
	}

	public String getNew_id() {
		return new_id;
	}

	public void setNew_id(String new_id) {
		this.new_id = new_id;
	}

	public Timestamp getApplyDate() {
		return ApplyDate;
	}

	public void setApplyDate(Timestamp applyDate) {
		ApplyDate = applyDate;
	}

	public Timestamp getVerifyDate() {
		return VerifyDate;
	}

	public void setVerifyDate(Timestamp verifyDate) {
		VerifyDate = verifyDate;
	}

	public int getFlg() {
		return flg;
	}

	public void setFlg(int flg) {
		this.flg = flg;
	}

	public int getApplyer() {
		return applyer;
	}

	public void setApplyer(int applyer) {
		this.applyer = applyer;
	}

	public String getCause() {
		return cause;
	}

	public void setCause(String cause) {
		this.cause = cause;
	}

}
