package vo;

public class Student {
  private int id;
  private String name;
  private String grate;
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public String getGrate() {
	return grate;
}
public void setGrate(String grate) {
	this.grate = grate;
}
  
}
