public class ReadMe {
	/**
	 * Hibernate中各个jar文件的含义：
	 * 
	 * Hibernate的主要类库：
	 * ----------// begin  //-----------------
	 * hibernate3.jar                                   Hibernate主类库
	 * commons-collections-3.1.jar                      Commons的集合类库
	 * antlr-2.7.6.jar                                  Java开源语法分析生成器
	 * dom4j-1.6.1.jar                                  解析XML的类库
	 * javassist-3.12.0.GA.jar                          开源的分析，编辑和创建Java自己码的类库
	 * slf4j-api-1.6.6.jar                              各种日志处理接口
	 * slf4j-simple-1.6.6.jar                           各种日志处理接口的实现
	 * jta-1.1.jar                                      Java中的事务处理API
	 * ----------// end 8个 //-----------------
	 *  
	 * 数据库连接类库：
	 * ----------// begin  //-----------------
	 * mysql-connector-java-5.1.22-bin.jar              mysql数据库连接类库
	 * ----------// end 1个 //-----------------
	 * 
	 * Hibernate的注解Annotation类库：
	 * ----------// begin  //-----------------
	 * hibernate-commons-annotations-4.0.1.Final.jar    
	 * hibernate-jpa-2.0-api-1.0.1.Final.jar
	 * ----------// end 2个 //-----------------
	 * 
	 * 
	 * 
	 * Hibernate的注解EntityManager类库：
	 * ----------// begin  //-----------------
	 * hibernate-entitymanager.jar
	 * jboss-archive-browsing.jar
	 * ----------// end 2个 //-----------------
	 * 
	 * 
	 * Hibernate的注解org/apache/commons/logging/LogFactory类库：
	 * 如果缺少，会出现：Exception in thread "main" java.lang.NoClassDefFoundError: org/apache/commons/logging/LogFactory 
	 * ----------// begin  //-----------------
	 * commons-logging-1.0.4.jar
	 * ----------// end 1个 //-----------------
	 * 
	 * 
	 * Hibernate的注解org/jboss/logging/BasicLogger类库：
	 * 如果缺少，会出现：java.lang.NoClassDefFoundError: org/jboss/logging/BasicLogger
	 * ----------// begin  //-----------------
	 * jboss-logging-3.0.0.GA.jar
	 * ----------// end 1个 //-----------------
	 * 
	 * 
	 * 共：15个
	 */
}
